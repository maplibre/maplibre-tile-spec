#![cfg(feature = "reader")]

use fast_mvt::{MvtError, MvtReaderRef};

#[test]
fn invalid_protobuf_wire_data_fails_at_decode() {
    let bytes = [0x1a, 0x02, 0x08];

    assert!(matches!(
        MvtReaderRef::new(&bytes),
        Err(MvtError::Decode(_))
    ));
}

#[test]
fn corrupted_geometry_tile_fails_when_parsing_layer() {
    let bytes = [
        0x1a, 0x19, 0x12, 0x14, 0x10, 0x3d, 0x20, 0x11, 0x20, 0x28, 0x20, 0x28, 0x20, 0x20, 0x20,
        0x20, 0x20, 0x38, 0x20, 0x20, 0x18, 0x03, 0x40, 0x11, 0x60, 0xef, 0x23,
    ];

    assert!(matches!(
        MvtReaderRef::new(&bytes),
        Err(MvtError::MissingLayerName)
    ));
}

#[test]
fn corrupted_tags_tile_fails_when_parsing_layer() {
    let bytes = [
        0x1a, 0x19, 0x12, 0x14, 0x10, 0x0, 0x10, 0x0, 0x20, 0x20, 0x10, 0x28, 0x20, 0x28, 0xe0,
        0xdf, 0x20, 0x20, 0x20, 0x32, 0x20, 0x20, 0x18, 0x1, 0x60, 0xef, 0x1b,
    ];

    assert!(matches!(
        MvtReaderRef::new(&bytes),
        Err(MvtError::MissingLayerName)
    ));
}
